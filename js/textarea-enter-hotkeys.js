$(function () {
    $('#fwid').focus();
    $('#fwid').keydown(function (event) {
        switch (event.which) {
            case 13:
                return false;
        }
    });

    $('#fwid').keyup(function (event) {
        switch (event.which) {
            case 13:
                $('#form').submit();
                return false;
                break;
        }
    });
    ;
});

