    <h2>Пункт первый: как выбрать багет для картины по размеру</h2>
    <p>Если картина небольшая, то стоит присмотреться к широким рамкам, однако при этом важно соблюсти пропорцию, багет не должен «перекрывать» по размеру изображение. Также он не должен отвлекать внимание от мелких деталей небольшого полотна, следовательно, багет должен быть минималистичным. Для большой картины багет можно выбрать более вычурный и более тонкий, снова учитывая размеры самой картины и деталей полотна.</p>
    <h2>Пункт второй: как выбрать багет для картины по тональности и цвету</h2>
    <p>Тональность и цвет играют огромную роль в выборе багета. Прежде всего, необходимо определить, какой тон является преобладающим в изображении – тёплый или холодный. Очень редко встречаются картины, сделанные в мультитональном формате. В этом случае можно поступить просто: выбрать один из нейтральных вариантов. Интересным решением является контрастирование, выделяющее определённые акценты полотна. То же самое касается и цвета: важно, чтобы он в точности соответствовал базовым цветам картины или гармонировал с ними.</p>
    <h2>Пункт третий: как подобрать багет к картине по стилю</h2>
